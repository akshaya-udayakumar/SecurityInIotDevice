package com.ilp.tcs.insurance_management.bean;

public class InsuranceProducts {
	
	private String InsProductName;
	private String InsProductStatus;
	private String Coverages;
	private int InsProductId;
	
	
	public String getInsProductName() {
		return InsProductName;
	}
	public void setInsProductName(String insProductName) {
		InsProductName = insProductName;
	}
	public String getInsProductStatus() {
		return InsProductStatus;
	}
	public void setInsProductStatus(String insProductStatus) {
		InsProductStatus = insProductStatus;
	}
	public String getCoverages() {
		return Coverages;
	}
	public void setCoverages(String coverages) {
		Coverages = coverages;
	}
	public int getInsProductId() {
		return InsProductId;
	}
	public void setInsProductId(int insProductId) {
		InsProductId = insProductId;
	}
	
	
	

}
